public class Lektion2 {

    // Første punkt i øvelsen for kommentarer, udskrift og variabler er denne 1 linies kommentar.

    /* Vi skal ligeledes lave en blok af kommentarer. En blok af kommentarer strækker sig over flere linier
       og indkapsles i /* og det omvendte til slut.
     */

    public static void main(String[] args) {

        // Sådan laves en udskrift til terminal
        System.out.println("Programmering er nemt hos nemprogrammering.dk");

        // Sådan erklæres en int variabel
        int x;
        // Sådan tildeles variablen en værdi
        x = 21;

        // Jeg kunne også have skrevet int x = 21;

        System.out.println("Min første variabel");

        // Flere variabler - her erklæres og tildeles de værdi på samme linie
        int y = -5;
        int z = 22;
        int i = 34789;

        // Udskrift til terminal - HUSK ""
        System.out.println("Jeg er god til at lave int variabler");

    }


}
